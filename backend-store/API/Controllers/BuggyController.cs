
using Microsoft.AspNetCore.Mvc;

namespace API.Controllers
{
    // Controller to handle or return errors.
    public class BuggyController : BaseApiController
    {
        [HttpGet("not-found")]
        public ActionResult GetNotFound()
        {
            return NotFound();
        }

        [HttpGet("bad-request")]
        public ActionResult GetBadRequest()
        {
            return BadRequest(new ProblemDetails { Title = "This is a bad request." });
        }

        [HttpGet("unauthorized")]
        public ActionResult GetUnauthorized()
        {
            return Unauthorized();
        }

        [HttpGet("validation-error")]
        public ActionResult GetValidationError()
        {
            // We have access to our model states for validation errors.
            ModelState.AddModelError("Problem1", "This is ThreadExceptionEventArgs first error");
            ModelState.AddModelError("Problem2", "This is ThreadExceptionEventArgs second error");
            return ValidationProblem();
        }

        [HttpGet("server-error")]
        public ActionResult GetServerError()
        {
            throw new Exception("This is a server error");
        }
    }
}